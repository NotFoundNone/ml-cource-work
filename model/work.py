import numpy as np

# Загружаем полученные параметры theta
theta = np.loadtxt('../normalize/data/theta/optimized_theta.csv', delimiter=',')  # Загружаем оптимизированные параметры

# Данные для одного студента (предоставленные вами)
student_data = [-0.1628100888674033,1.6473343222141081,-0.12425713438035438,1.2884759823788006,-1.2145927942567014,-0.7009372046241013,0.6201112827637236,0.1344315011826357,0.2858037283291292,-0.40121132001337423,0.2859491170949895,1.3381910911140202,-0.6609561016958618,-0.2524993442221403,0.03140895357277211,-0.3428406587266401,-0.8936742460575363,-0.7483504878071848,-0.8556817210834914]

# Преобразуем данные студента в массив
X_student = np.array(student_data).reshape(1, -1)

# Добавляем единичный столбец для учёта смещения (bias)
X_student = np.c_[np.ones(X_student.shape[0]), X_student]  # Добавляем единичный столбец

# Функция для предсказания
def predict(X, theta):
    return np.dot(X, theta)

# Предсказание для этого студента
predicted_exam_score = predict(X_student, theta)

# Выводим предсказанное значение
print(f'Предсказанный балл экзамена для студента: {predicted_exam_score[0]}')
